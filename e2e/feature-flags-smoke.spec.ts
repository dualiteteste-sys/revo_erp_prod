test('CFG-04: Feature Flags (NF-e) salva por admin', async ({ page }) => {
  test.setTimeout(60_000);

  let nfeEnabled = false;

  await page.route('**/rest/v1/empresa_features*', async (route) => {
    await route.fulfill({
      json: {
        empresa_id: 'empresa-1',
        revo_send_enabled: false,
        nfe_emissao_enabled: nfeEnabled,
        plano_mvp: 'ambos',
        max_users: 999,
        servicos_enabled: true,
        industria_enabled: true,
      },
    });
  });
  await page.route('**/rest/v1/rpc/empresa_features_get*', async (route) => {
    await route.fulfill({
      json: [
        {
          empresa_id: 'empresa-1',
          revo_send_enabled: false,
          nfe_emissao_enabled: nfeEnabled,
          plano_mvp: 'ambos',
          max_users: 999,
          servicos_enabled: true,
          industria_enabled: true,
        },
      ],
    });
  });

  await page.route('**/rest/v1/empresa_feature_flags*', async (route) => {
    const req = route.request();
    if (req.method() === 'OPTIONS') {
      await route.fulfill({ status: 204, body: '' });
      return;
    }

    if (req.method() === 'POST' || req.method() === 'PATCH') {
      const body = (await req.postDataJSON()) as any;
      const row = Array.isArray(body) ? body[0] : body;
      nfeEnabled = !!row?.nfe_emissao_enabled;
      await route.fulfill({ json: [row] });
      return;
    }

    await route.fulfill({ json: [] });
  });

  await page.route('**/rest/v1/**', async (route) => {
    if (route.request().method() === 'OPTIONS') {
      await route.fulfill({ status: 204, body: '' });
      return;
    }
    const url = route.request().url();
    if (
      url.includes('/rest/v1/rpc/empresas_list_for_current_user') ||
      url.includes('/rest/v1/rpc/active_empresa_get_for_current_user')
    ) {
      await route.fallback();
      return;
    }
    await route.fulfill({ json: [] });
  });

  await mockAuthAndEmpresa(page, { role: 'admin' });

  await page.goto('/auth/login');
  await page.getByPlaceholder('seu@email.com').fill('test@example.com');
  await page.getByLabel('Senha').fill('password123');
  await page.getByRole('button', { name: 'Entrar' }).click();
  await expect(page).toHaveURL(/\/app\//);

  await page.goto('/app/configuracoes/avancado/feature-flags');
  await expect(page.getByRole('heading', { name: 'Feature Flags' })).toBeVisible();

  // Alternar NF-e
  await page.getByRole('button', { name: 'Alternar emiss√£o de NF-e' }).click();
  await page.getByRole('button', { name: 'Salvar' }).click();

  await expect(page.getByText('Feature flags salvas.')).toBeVisible();
});
